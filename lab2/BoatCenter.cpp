#include "BoatCenter.h"
#include "Combi.h"

using namespace combi;


int boat_center(
    short m,            // количество мест на судне
    short n,            // всего контейнеров
    const int v[],      // веса контейнеров
    const int c[],      // доходы от перевозки
    const int min_w[],  // минимальный вес для каждого места
    const int max_w[],  // максимальный вес для каждого места
    short result[]      // результат: индексы выбранных контейнеров
)
{
    // Используем генератор сочетаний для перебора всех вариантов выбора контейнеров
    // Создаем генератор сочетаний: выбираем m контейнеров из n
    xcombination comb(n, m);

    int maxProfit = 0;      // максимальная прибыль
    short best[10];         // лучшее сочетание контейнеров

    // Перебираем все возможные сочетания
    int k = comb.getfirst();
    while (k > 0)
    {
        bool valid = true;  // флаг корректности текущего сочетания
        int profit = 0;     // прибыль от текущего сочетания

        // Проверяем каждый контейнер в текущем сочетании
        for (int i = 0; i < m; i++)
        {
            int idx = comb.ntx(i); // индекс контейнера в исходном массиве

            // Проверяем ограничения по весу для i-го места
            if (v[idx] < min_w[i] || v[idx] > max_w[i])
            {
                valid = false; // контейнер не подходит для этого места
                break;
            }

            // Суммируем прибыль
            profit += c[idx];
        }

        // Если сочетание корректно и прибыль больше текущей максимальной
        if (valid && profit > maxProfit)
        {
            maxProfit = profit;
            // Сохраняем лучшее сочетание
            for (int i = 0; i < m; i++)
                best[i] = comb.ntx(i);
        }

        // Переходим к следующему сочетанию
        k = comb.getnext();
    }

    // Копируем результат в выходной массив
    for (int i = 0; i < m; i++)
        result[i] = best[i];

    return maxProfit; // возвращаем максимальную прибыль
}