// ================================================================
// Расстановка скобок при перемножении матриц
// Матрица i имеет размерность c[i-1] x c[i]  (1-based нумерация)
// Массив s[n*n] хранит позиции разбиения: s[(i-1)*n+(j-1)] = k
// ================================================================

// Рекурсивный метод (без мемоизации)
// i, j — индексы первой и последней матрицы (1-based)
int matrix_chain_r(int i, int j, int n, const int c[], int* s);

// Динамическое программирование
int matrix_chain_dp(int n, const int c[], int* s);

// Вывести оптимальную расстановку скобок по матрице S
void print_brackets(int i, int j, int n, const int* s);

// Вывести матрицу S в консоль
void print_s_matrix(int n, const int* s);